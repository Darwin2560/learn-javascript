// Variables
let n1, n2, cociente, residuo

// Entrada de datos
n1 = parseFloat(prompt("Ingrese el primer número"))
n2 = parseFloat(prompt("Ingrese el segundo número"))

// Operaciones
cociente = n1 / n2
residuo = n1 % n2

// Salida de resultados
document.write('Cociente: ' + cociente + '<br>')
document.write('Residuo: ' + residuo)